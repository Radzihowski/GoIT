# При аналізі даних часто виникає необхідність позбавитися екстремальних значень, перш ніж почати працювати з даними
# далі. Напишіть функцію prepare_data, яка видаляє з переданого списку найбільше та найменше значення, сортує його в
# порядку зростання і повертає змінений список як результат.

def prepare_data(data):
    new_data = sorted(data)
    return new_data[1:-1]

print(prepare_data([1, 3.1415, 41, 3]))